/*There are 4 common methods to round a number to an integer:

Math.round(x)	Returns x rounded to its nearest integer
Math.ceil(x)	Returns x rounded up to its nearest integer
Math.floor(x)	Returns x rounded down to its nearest integer
Math.trunc(x)	Returns the integer part of x 
*/
console.log(`use round for 4.4 : ${Math.round(4.4)}`);
console.log(`use round for 4.5 : ${Math.round(4.5)}`);
console.log(`use round for 4.6 : ${Math.round(4.6)}`);
console.log("**************************************");
console.log(`use ceil for 4.4 : ${Math.ceil(4.4)}`);
console.log(`use ceil for 4.5 : ${Math.ceil(4.5)}`);
console.log(`use ceil for 4.6 : ${Math.ceil(4.6)}`);
console.log("**************************************");
console.log(`use of floor for 4.4: ${Math.floor(4.4)}`);
console.log(`use of floor for 4.5: ${Math.floor(4.5)}`);
console.log(`use of floor for 4.6: ${Math.floor(4.6)}`);
console.log("**************************************");
console.log(`use of trunc for 4.4: ${Math.trunc(24.4)}`);
console.log(`use of trunc for 4.5: ${Math.trunc(4.5)}`);
console.log(`use of trunc for 4.6: ${Math.trunc(4.6)}`);


