/* JavaScript Template Literals
     ::   Template Literals : 
     ::   Template Strings  : 
     ::   String Templates  : 
     ::   Back-Tics Syntax  : Template Literals use back-ticks (``) rather than the quotes ("") to define a string
     we can use single and double quotes inside the back-tics syntax


*/

// ::   Back-Tics Syntax  : Template Literals use back-ticks (``) rather than the quotes ("") to define a string
var text = `Hello guys, i'm Babloo Kumar`;
console.log(text);
var text = `Hello guys, "i'm Babloo Kumar"`;
// multiples lines
// ${} --> called interpolation
console.log(text);
var text =
`The quick
"brown fox"
jumps over
'the lazy dog'`;
console.log(text);
